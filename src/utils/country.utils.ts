import axios from 'axios';
import sharp from 'sharp';
import fs from 'fs/promises';
import path from 'path';

const IMAGE_PATH = path.join(__dirname, '../../cache/summary.png');
const WIDTH = 1200;
const HEIGHT = 600;

function currencyFormatted(num: number | null | undefined): string {
    if (num === null || num === undefined) return 'N/A';
    return Number(num).toLocaleString(undefined, { maximumFractionDigits: 2 });
}

function escapeHtml(unsafe: string): string {
    return String(unsafe).replace(/[&<>"']/g, function (m) {
        return (
            {
                '&': '&amp;',
                '<': '&lt;',
                '>': '&gt;',
                '"': '&quot;',
                "'": '&#039;',
            } as Record<string, string>
        )[m];
    });
}

export const fetchCountries = async () => {
    try {
        const response = await axios.get(
            'https://restcountries.com/v2/all?fields=name,capital,region,population,flag,currencies',
            { timeout: 5000 }
        );
        return response.data;
    } catch (error) {
        console.error('Error fetching countries:', error);
        return null;
    }
};

export const fetchExchangeRates = async () => {
    try {
        const response = await axios.get(
            'https://open.er-api.com/v6/latest/USD',
            { timeout: 5000 }
        );
        return response.data.rates;
    } catch (error) {
        console.error('Error fetching exchange rates:', error);
        return null;
    }
};

export const generateSummaryImage = async (
    totalCountries: number,
    top5Gdp: any[],
    lastRefreshed: Date
) => {
    const topList = top5Gdp
        .map((t, i) => {
            return `<tspan x="40" dy="${i === 0 ? 0 : 1.4}em">${i + 1}. ${escapeHtml(t.name)} â€” ${currencyFormatted(t.estimated_gdp)}</tspan>`;
        })
        .join('');

    const last = lastRefreshed.toISOString();

    const svg = `
    <svg width="${WIDTH}" height="${HEIGHT}" xmlns="http://www.w3.org/2000/svg">
        <style>
        .title { font-family: Arial, Helvetica, sans-serif; font-size: 36px; font-weight: bold; fill: #222; }
        .meta { font-family: Arial, Helvetica, sans-serif; font-size: 18px; fill: #444; }
        .list { font-family: Arial, Helvetica, sans-serif; font-size: 22px; fill: #111; }
        rect { fill: #f7f7f8; }
        </style>
        <rect width="100%" height="100%" rx="12" />
        <text x="40" y="70" class="title">Country Summary</text>
        <text x="40" y="110" class="meta">Total countries: ${totalCountries}</text>
        <text x="40" y="140" class="meta">Last refreshed: ${escapeHtml(last)}</text>

        <g transform="translate(40, 190)" class="list">
        ${topList || '<tspan x="0">No GDP data available</tspan>'}
        </g>

        <text x="${WIDTH - 40}" y="${HEIGHT - 40}" class="meta" text-anchor="end">Generated by Country API</text>
    </svg>`;

    // Ensure directory exists
    const dir = path.dirname(IMAGE_PATH);
    await fs.mkdir(dir, { recursive: true });

    // Convert SVG to PNG with sharp
    const svgBuffer = Buffer.from(svg);
    await sharp(svgBuffer).resize(WIDTH, HEIGHT).png().toFile(IMAGE_PATH);
};
